# Benchmark (SKILL.md: GB/s for memory-bound kernels)
# ============================================================

@triton.testing.perf_report(
    triton.testing.Benchmark(
        x_names=['N'],
        x_vals=[128 * i for i in range(2, 80)],
        line_arg='provider',
        line_vals=['triton', 'torch'],
        line_names=['Triton', 'PyTorch'],
        styles=[('blue', '-'), ('green', '-')],
        ylabel='GB/s',
        plot_name='layernorm-performance',
        args={'M': 4096},
    ))
def benchmark(M, N, provider):
    x = torch.randn(M, N, device=DEVICE, dtype=torch.float32)
    weight = torch.randn(N, device=DEVICE, dtype=torch.float32)
    bias = torch.randn(N, device=DEVICE, dtype=torch.float32)

    stream = torch.cuda.Stream()
    torch.cuda.set_stream(stream)

    if provider == 'torch':
        ms = triton.testing.do_bench(lambda: torch.nn.functional.layer_norm(x, [N], weight=weight, bias=bias))
    elif provider == 'triton':
        ms = triton.testing.do_bench(lambda: triton_layernorm(x, weight=weight, bias=bias))

    # Memory: read x + weight + bias, write output
    # Approximate as 2 * M * N (dominant terms)
    gbps = 2 * x.numel() * x.element_size() * 1e-9 / (ms * 1e-3)
    return gbps


if __name__ == "__main__":
    test_layernorm()

    import sys
    if len(sys.argv) > 1 and sys.argv[1] == "--benchmark":
        benchmark.run(save_path='.', print_data=True)
