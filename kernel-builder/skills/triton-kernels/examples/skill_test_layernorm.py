# Correctness test (SKILL.md: always validate before benchmarking)
# ============================================================

def test_layernorm():
    print("=" * 60)
    print("LayerNorm Correctness Tests")
    print("=" * 60)

    torch.manual_seed(0)
    test_cases = [
        ((1823, 781), "irregular shape"),
        ((1, 1), "single element"),
        ((4096, 4096), "large square"),
        ((32, 127), "small irregular"),
        ((2048, 2048), "power of 2"),
    ]

    # Test WITHOUT weight/bias
    print("\nWithout affine parameters:")
    for shape, desc in test_cases:
        x = torch.randn(shape, device=DEVICE, dtype=torch.float32)
        y_triton = triton_layernorm(x, eps=1e-5)
        y_ref = torch.nn.functional.layer_norm(x, [shape[-1]], eps=1e-5)
        try:
            torch.testing.assert_close(y_triton, y_ref, atol=1e-3, rtol=1e-3)
            print(f"  PASSED: {desc} {shape}")
        except AssertionError as e:
            print(f"  FAILED: {desc} {shape}: {e}")

    # Test WITH weight and bias
    print("\nWith affine parameters:")
    for shape, desc in test_cases:
        D = shape[-1]
        x = torch.randn(shape, device=DEVICE, dtype=torch.float32)
        weight = torch.randn(D, device=DEVICE, dtype=torch.float32)
        bias = torch.randn(D, device=DEVICE, dtype=torch.float32)
        y_triton = triton_layernorm(x, weight=weight, bias=bias, eps=1e-5)
        y_ref = torch.nn.functional.layer_norm(x, [D], weight=weight, bias=bias, eps=1e-5)
        try:
            torch.testing.assert_close(y_triton, y_ref, atol=1e-3, rtol=1e-3)
            print(f"  PASSED: {desc} {shape}")
        except AssertionError as e:
            print(f"  FAILED: {desc} {shape}: {e}")

    # Test bf16
    print("\nBFloat16:")
    x = torch.randn(1024, 1024, device=DEVICE, dtype=torch.bfloat16)
    weight = torch.randn(1024, device=DEVICE, dtype=torch.bfloat16)
    bias = torch.randn(1024, device=DEVICE, dtype=torch.bfloat16)
    y_triton = triton_layernorm(x, weight=weight, bias=bias, eps=1e-5)
    y_ref = torch.nn.functional.layer_norm(x, [1024], weight=weight, bias=bias, eps=1e-5)
    try:
        torch.testing.assert_close(y_triton, y_ref, atol=1e-2, rtol=1e-2)
        print(f"  PASSED: bf16 (1024, 1024)")
    except AssertionError as e:
        print(f"  FAILED: bf16: {e}")

    print()


