"""
LayerNorm kernel generated by an AI agent using the triton-kernels skill.

This kernel was written following the patterns in:
    kernel-builder/skills/triton-kernels/SKILL.md

Demonstrates:
- Row-wise reduction (mean + variance)
- fp32 accumulation
- Masked loads with other=0.0
- Correctness test against torch.nn.functional.layer_norm
- Benchmark harness (GB/s)
"""

import torch
import triton
import triton.language as tl

DEVICE = torch.device(f'cuda:{torch.cuda.current_device()}')


@triton.jit
def layernorm_kernel(
    x_ptr, weight_ptr, bias_ptr, out_ptr,
    stride_row, D,
    eps: tl.constexpr,
    HAS_WEIGHT: tl.constexpr,
    HAS_BIAS: tl.constexpr,
    BLOCK_D: tl.constexpr,
):
    # One program per row (following SKILL.md reduction pattern)
    row = tl.program_id(0)
    offsets = tl.arange(0, BLOCK_D)
    mask = offsets < D

    # Load row into SRAM, accumulate in fp32 (SKILL.md: Hard Constraint #3)
    x = tl.load(x_ptr + row * stride_row + offsets, mask=mask, other=0.0).to(tl.float32)

    # Compute mean
    mean = tl.sum(x, axis=0) / D

    # Compute variance — zero out masked positions so they don't contribute
    x_centered = tl.where(mask, x - mean, 0.0)
    variance = tl.sum(x_centered * x_centered, axis=0) / D

    # Normalize
    inv_std = tl.rsqrt(variance + eps)
    x_norm = x_centered * inv_std

    # Apply affine transform if weight/bias exist
    if HAS_WEIGHT:
        w = tl.load(weight_ptr + offsets, mask=mask, other=1.0).to(tl.float32)
        x_norm = x_norm * w
    if HAS_BIAS:
        b = tl.load(bias_ptr + offsets, mask=mask, other=0.0).to(tl.float32)
        x_norm = x_norm + b

    # Store back in input dtype (SKILL.md: cast back only at final store)
    tl.store(out_ptr + row * stride_row + offsets, x_norm, mask=mask)


def triton_layernorm(x, weight=None, bias=None, eps=1e-5):
    """LayerNorm wrapper following SKILL.md conventions."""
    # Hard Constraint #4: assert contiguous
    assert x.is_contiguous()

    x_2d = x.view(-1, x.shape[-1])
    out = torch.empty_like(x_2d)
    M, D = x_2d.shape

    has_weight = weight is not None
    has_bias = bias is not None
    if not has_weight:
        weight = torch.empty(0, device=x.device)
    if not has_bias:
        bias = torch.empty(0, device=x.device)

    # Hard Constraint #1: BLOCK_D covers full reduction dimension
    BLOCK_D = triton.next_power_of_2(D)
    # num_warps heuristic from SKILL.md
    num_warps = 4 if BLOCK_D <= 1024 else (8 if BLOCK_D <= 4096 else 16)

    layernorm_kernel[(M,)](
        x_2d, weight, bias, out,
        x_2d.stride(0), D, eps,
        has_weight, has_bias,
        BLOCK_D=BLOCK_D, num_warps=num_warps, num_stages=2,
    )
    return out.view_as(x)


